// Task 4.1: Circle area
const radius = 5;
const circleArea = Math.PI * radius ** 2;
console.log(circleArea.toFixed(2));

// Task 4.2: Rectangle area
const length = 10;
const width = 4;
const rectangleArea = length * width;
console.log(rectangleArea.toFixed(2));

// Task 4.3: Cylinder volume
const cylinderRadius = 3;
const height = 8;
const cylinderVolume = Math.PI * cylinderRadius ** 2 * height;
console.log(cylinderVolume.toFixed(2));
